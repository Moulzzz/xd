import math


def area(r):
    '''
    Returns the area of a circle.
    :param r: radius - integer decimal number
    :return: integer decimal number
    '''
    return math.pi * r * r


def perimeter(r):
    '''
    Returns the perimeter of a circle.
    :param r: radius - integer decimal number
    :return: integer decimal number
    '''
    return 2 * math.pi * r

