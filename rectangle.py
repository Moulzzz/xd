def area(a, b):
    '''
    Returns the area of a rectangle.
    :param a: first edge - integer decimal number
    :param b: second edge - integer decimal number
    :return: area - integer decimal number
    '''
    return a * b 

def perimeter(a, b):
    '''
    Returns the perimeter of a rectangle.
    :param a: first edge - integer decimal number
    :param b: second edge - integer decimal number
    :return: perimeter - integer decimal number
    '''
    return (a + b) * 2 
