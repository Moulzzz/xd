def area(a, h):
    '''
    Returns the perimeter of a triangle.
    :param a: base of triangle - integer decimal number
    :param h: height of triangle - integer decimal number
    :return: integer decimal number
    '''
    return a * h / 2 

def perimeter(a, b, c):
    '''
    Returns the perimeter of a triangle.
    :param a: first integer decimal number
    :param b: second integer decimal number
    :param c: third integer decimal number
    :return: integer decimal of sum
    '''
    return a + b + c 
