
def area(a):
    '''
    Returns the area of a square.
    :param a: edge of square - integer decimal number
    :return: area of square - integer decimal number
    '''
    return a * a


def perimeter(a):
    '''
    Returns the perimeter of a square.
    :param a: edge of square - integer decimal number
    :return: perimeter of square - integer decimal number
    '''
    return 4 * a
